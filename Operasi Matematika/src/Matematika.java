public class Matematika {
    static int Penjumlahan(int x, int y) {
    return x + y;
  }
    static int Pengurangan(int x, int y) {
    return x - y;
  }
    static int Perkalian(int x, int y) {
    return x * y;
  }
    static int Pembagian(int x, int y) {
    return x / y;
  }
}
